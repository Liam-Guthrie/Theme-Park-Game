public class Player {

    // Sets up different variables for the player

    int lowDamage = 0;
    int okDamage = 0;
    int points = 0;
    int health = 0;

    public Player(int lowDamage, int okDamage, int points, int health) {
        this.lowDamage = lowDamage;
        this.okDamage = okDamage;
        this.points = points;
        this.health = health;
    }

}